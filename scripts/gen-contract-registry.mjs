#!/usr/bin/env node
// =============================================================================
// gen-contract-registry.mjs — RD-0359: generate ALL governed .fungi flow contracts into ONE doc.
//
// Every Galerina flow carries a `contract { intent {…} effects {…} privacy {…} }`. Scattered across
// ~446 .fungi files, the governed surface has no single index. This tool builds one: it parses every
// .fungi with the REAL compiler parser (parseProgram — ZT-62, never regex for the flow list), collects
// each flow's (qualifier, signature, declared effects), and extracts the human `intent` string from a
// bounded source scan keyed by the parser's own flow location. Output: a single registry doc + JSON.
//
// Output: docs/contract-registry/CONTRACT_REGISTRY.md (human) + contract-registry.json (machine).
// Usage:  node scripts/gen-contract-registry.mjs            (regenerate)
//         node scripts/gen-contract-registry.mjs --check    (fail if the on-disk doc is stale — CI)
//         node scripts/gen-contract-registry.mjs --self-test (anti-vacuous: prove it extracts a contract)
// =============================================================================
import { readFileSync, readdirSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join, dirname, relative } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const COMPILER = join(ROOT, "packages-galerina/galerina-core-compiler/dist/index.js");
const OUT_DIR = join(ROOT, "docs/contract-registry");
const SKIP = new Set(["node_modules", "dist", "build", ".git", ".graph", ".galerina"]);

function walkFungi(dir, acc) {
  let entries;
  try { entries = readdirSync(dir, { withFileTypes: true }); } catch { return acc; }
  for (const e of entries) {
    if (e.name.startsWith(".") && e.name !== ".") continue;
    const p = join(dir, e.name);
    if (e.isDirectory()) { if (!SKIP.has(e.name)) walkFungi(p, acc); }
    else if (e.isFile() && e.name.endsWith(".fungi")) acc.push(p);
  }
  return acc;
}

// The `intent { "…" }` string that follows a flow's declaration (bounded, keyed by the parser's offset).
function intentAt(src, offset) {
  const window = src.slice(offset, offset + 3000);
  const m = window.match(/intent\s*\{\s*"((?:[^"\\]|\\.)*)"/);
  return m ? m[1].replace(/\s+/g, " ").trim() : "";
}

async function collect() {
  const L = await import(pathToFileURL(COMPILER).href);
  const files = walkFungi(ROOT, []).sort();
  const rows = [];
  let parsed = 0, parseFail = 0;
  for (const f of files) {
    let src;
    try { src = readFileSync(f, "utf8"); } catch { continue; }
    if (src.charCodeAt(0) === 0xFEFF) src = src.slice(1);
    let prog;
    try { prog = L.parseProgram(src, f); } catch { parseFail++; continue; }
    parsed++;
    const rel = relative(ROOT, f).replace(/\\/g, "/");
    for (const fl of prog.flows ?? []) {
      rows.push({
        file: rel, name: fl.name, qualifier: fl.qualifier ?? "flow",
        signature: `${fl.qualifier && fl.qualifier !== "flow" ? fl.qualifier + " " : ""}flow ${fl.name}(${(fl.params ?? []).join(", ")}) -> ${fl.returnType ?? "?"}`,
        effects: fl.declaredEffects ?? [], intent: intentAt(src, fl.location?.offset ?? 0),
      });
    }
  }
  return { rows, files: files.length, parsed, parseFail };
}

// top-level corpus bucket for grouping (examples/curriculum vs package self-hosted twins vs showcase …)
function bucket(rel) {
  if (rel.startsWith("docs/examples/")) return "docs/examples (curriculum)";
  if (rel.startsWith("examples/")) return "examples (showcase)";
  const m = rel.match(/^packages-galerina\/([^/]+)\//);
  if (m) return `package: ${m[1]}`;
  return rel.split("/").slice(0, 2).join("/");
}

function render({ rows, files, parsed, parseFail }) {
  const byQual = {};
  for (const r of rows) byQual[r.qualifier] = (byQual[r.qualifier] ?? 0) + 1;
  const groups = new Map();
  for (const r of rows) { const b = bucket(r.file); if (!groups.has(b)) groups.set(b, []); groups.get(b).push(r); }
  let md = `# Contract registry (RD-0359)\n\n`;
  md += `> Generated by \`scripts/gen-contract-registry.mjs\` — every governed \`.fungi\` flow contract in one place.\n`;
  md += `> Flow list is parser-authoritative (\`parseProgram\`); the \`intent\` string is extracted from source at the parser's flow location.\n\n`;
  md += `**${rows.length} contracts** across **${parsed} parsed \`.fungi\`** (${files} scanned${parseFail ? `, ${parseFail} negative/unparseable skipped` : ""}). `;
  md += `By qualifier: ${Object.entries(byQual).sort((a, b) => b[1] - a[1]).map(([q, n]) => `\`${q}\` ${n}`).join(" · ")}.\n\n`;
  for (const [b, rs] of [...groups.entries()].sort((a, b2) => b2[1].length - a[1] < 0 ? 1 : b2[1].length === a[1].length ? a[0].localeCompare(b2[0]) : -1)) {
    md += `## ${b} — ${rs.length}\n\n| Flow | Contract (signature) | Effects | Intent |\n|---|---|---|---|\n`;
    for (const r of rs.sort((x, y) => x.file.localeCompare(y.file) || x.name.localeCompare(y.name))) {
      const eff = r.effects.length ? r.effects.map((e) => `\`${e}\``).join(" ") : "—";
      const intent = r.intent ? (r.intent.length > 120 ? r.intent.slice(0, 117) + "…" : r.intent) : "—";
      md += `| \`${r.name}\` | \`${r.signature}\` | ${eff} | ${intent.replace(/\|/g, "\\|")} |\n`;
    }
    md += `\n`;
  }
  return md;
}

// ── modes ──
if (process.argv.includes("--self-test")) {
  const { rows } = await collect();
  const sample = rows.find((r) => r.name === "admitVerdict" && r.file.includes("registry-index"));
  const ok = rows.length > 100 && sample && sample.intent.length > 0 && sample.qualifier === "pure";
  console.log(ok ? `✅ self-test: ${rows.length} contracts, sample admitVerdict intent extracted` : "❌ self-test FAILED (vacuous or missing intent)");
  process.exit(ok ? 0 : 1);
}

const data = await collect();
const md = render(data);
const json = JSON.stringify({ generatedBy: "gen-contract-registry.mjs", contracts: data.rows.length, parsed: data.parsed, rows: data.rows }, null, 2);
const mdPath = join(OUT_DIR, "CONTRACT_REGISTRY.md");
const jsonPath = join(OUT_DIR, "contract-registry.json");

if (process.argv.includes("--check")) {
  const stale = !existsSync(mdPath) || readFileSync(mdPath, "utf8") !== md;
  console.log(stale ? "❌ contract-registry: on-disk doc is STALE — run `node scripts/gen-contract-registry.mjs`" : `✅ contract-registry: up to date (${data.rows.length} contracts)`);
  process.exit(stale ? 1 : 0);
}

if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(mdPath, md);
writeFileSync(jsonPath, json);
console.log(`✅ contract-registry: ${data.rows.length} contracts across ${data.parsed} .fungi → docs/contract-registry/CONTRACT_REGISTRY.md + .json`);
