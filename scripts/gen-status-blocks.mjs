#!/usr/bin/env node
// gen-status-blocks.mjs — the DURABLE fix for status drift (task #74). component-health.mjs is the ONE
// source of truth for the ship-readiness %, the Zero-Trust-thesis boundaries, and the Build-progress
// layers. Docs (roadmap, README) kept drifting because their status numbers were hand-copied. This
// tool GENERATES the status block FROM component-health and owns a marker-delimited region in the doc;
// a `--check` drift gate REDs the moment the doc's block diverges from the live source — so a number
// can never silently go stale again (the same generate-then-gate discipline as doc:wat-drift).
//
//   node scripts/gen-status-blocks.mjs                # print the generated block to stdout
//   node scripts/gen-status-blocks.mjs --write        # inject it between the markers in the target doc(s)
//   node scripts/gen-status-blocks.mjs --check         # exit 1 if any target's block is stale (the gate)
//   node scripts/gen-status-blocks.mjs --self-test     # prove generate + drift detection fire
import { readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { spawnSync } from "node:child_process";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const CH = join(ROOT, "scripts", "component-health.mjs");
const BEGIN = "<!-- STATUS:BEGIN (generated by scripts/gen-status-blocks.mjs — do not edit; run `node scripts/gen-status-blocks.mjs --write`) -->";
const END = "<!-- STATUS:END -->";
// Docs that carry a generated status block (must contain the BEGIN/END markers).
const TARGETS = ["docs/roadmap-2026-07-15.md", "docs/roadmap-2026-07-23.md"];
const ARTIFACT = join(ROOT, "build", "status", "STATUS.md");

// The single source: component-health's --json (percentAudit is structurally enforced to hold all 3 sections).
function audit() {
  const r = spawnSync(process.execPath, [CH, "--json"], { cwd: ROOT, encoding: "utf8", windowsHide: true, maxBuffer: 32 * 1024 * 1024 });
  if (r.status !== 0) throw new Error(`component-health --json failed (exit ${r.status})`);
  return JSON.parse(r.stdout).percentAudit;
}

const stateOf = (s) => (typeof s === "number" ? "building" : s);

// Deterministic markdown — no timestamps (a clock would make the block drift every run and defeat the gate).
export function renderBlock(pa) {
  const zt = pa.sections.find((s) => s.key === "zero-trust-thesis");
  const bp = pa.sections.find((s) => s.key === "build-progress");
  const reg = pa.sections.find((s) => s.key === "tracking-registry");
  const tally = {};
  for (const r of reg.rows) tally[stateOf(r.state)] = (tally[stateOf(r.state)] ?? 0) + 1;
  const order = ["shipped", "building", "build-pending", "post-v1"];
  const tallyStr = order.filter((k) => tally[k]).map((k) => `${tally[k]} ${k}`).join(" · ")
    + Object.keys(tally).filter((k) => !order.includes(k)).map((k) => ` · ${tally[k]} ${k}`).join("");
  const meterRows = (rows) => rows.filter((r) => typeof r.pct === "number").map((r) => `| ${r.label} | ${r.pct}% |`).join("\n");
  return [
    BEGIN,
    `**Ship-readiness ${pa.shipReadinessPct.toFixed(1)}%** · **Zero-Trust thesis avg ${pa.ztAvg}%** · **Build progress avg ${pa.buildAvg}%** · Tracking registry (${pa.trackingRegistryCount}): ${tallyStr}.`,
    ``,
    `| Zero-Trust boundary | % |`,
    `|---|--:|`,
    meterRows(zt.rows),
    ``,
    `| Build-progress layer | % |`,
    `|---|--:|`,
    meterRows(bp.rows),
    END,
  ].join("\n");
}

function inject(text, block) {
  const b = text.indexOf(BEGIN), e = text.indexOf(END);
  if (b < 0 || e < 0) return null; // no markers
  return text.slice(0, b) + block + text.slice(e + END.length);
}
function currentBlock(text) {
  const b = text.indexOf(BEGIN), e = text.indexOf(END);
  return b < 0 || e < 0 ? null : text.slice(b, e + END.length);
}

if (process.argv.includes("--self-test")) {
  const ok = (c, m) => { console.log(`  ${c ? "✅" : "❌"} ${m}`); if (!c) process.exitCode = 1; };
  const block = renderBlock(audit());
  ok(block.startsWith(BEGIN) && block.trimEnd().endsWith(END), "block is marker-delimited");
  ok(/Ship-readiness \d/.test(block) && /Zero-Trust thesis avg \d+%/.test(block) && /Build-progress layer/.test(block), "block carries all three status sections");
  ok(renderBlock(audit()) === block, "generation is DETERMINISTIC (no clock — the gate can't false-positive)");
  // drift detector: a doc whose block was hand-edited must be detected as stale.
  const stale = `pre\n${block.replace(/avg \d+%/, "avg 999%")}\npost`;
  ok(inject(stale, block) !== null && currentBlock(stale) !== block, "drift detector: a mutated block differs from the freshly generated one");
  console.log(process.exitCode ? "  gen-status self-test FAILED" : "  gen-status self-test: generate + determinism + drift detection verified ✅");
  process.exit(process.exitCode ?? 0);
}

const block = renderBlock(audit());
mkdirSync(dirname(ARTIFACT), { recursive: true });
writeFileSync(ARTIFACT, block + "\n"); // the canonical tracked artifact (always regenerated)

if (process.argv.includes("--check")) {
  let stale = [];
  for (const rel of TARGETS) {
    const abs = join(ROOT, rel);
    if (!existsSync(abs)) continue;
    const cur = currentBlock(readFileSync(abs, "utf8"));
    if (cur === null) { console.error(`  ⚠ ${rel}: no STATUS markers — run --write once to seed them`); continue; }
    if (cur !== block) stale.push(rel);
  }
  if (stale.length) {
    console.error(`\n  ❌ status-drift: ${stale.length} doc(s) have a STALE status block (source: component-health):\n${stale.map((r) => `     ${r}`).join("\n")}`);
    console.error(`  Fix: node scripts/gen-status-blocks.mjs --write  (regenerates from component-health — never hand-edit the block)`);
    process.exit(1);
  }
  console.log(`  ✅ status-drift: all ${TARGETS.length} target(s) match component-health.`);
  process.exit(0);
}

if (process.argv.includes("--write")) {
  let wrote = 0, missing = [];
  for (const rel of TARGETS) {
    const abs = join(ROOT, rel);
    if (!existsSync(abs)) continue;
    const text = readFileSync(abs, "utf8");
    const next = inject(text, block);
    if (next === null) { missing.push(rel); continue; }
    if (next !== text) { writeFileSync(abs, next); wrote++; }
  }
  console.log(`  status: wrote build/status/STATUS.md + updated ${wrote} doc(s)${missing.length ? ` · MISSING markers (seed manually once): ${missing.join(", ")}` : ""}`);
  process.exit(0);
}

console.log(block);
